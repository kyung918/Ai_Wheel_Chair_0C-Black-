void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{

	if(htim->Instance == TIM6)	//10ms timer
	{
		msec++;

		if(msec>=200){
		 msec=0;
		 sec++;

		 OPER;
		 if(sec>=60){
			sec=0;
			min++;
			if(min>=60){
			   min=0;
			   hour++;
			   if(hour>=24){
			   hour=1;
			   }
			}
		 }
		}
		
		
		if(utim.uart_get[0])utim.uart_get[0]--;
		if(utim.uart_get[1])utim.uart_get[1]--;
		if(utim.uart_get[2])utim.uart_get[2]--;
		if(utim.uart_get[3])utim.uart_get[3]--;
		if(utim.uart_get[4])utim.uart_get[4]--;

		if(adctim.adc_get[0])adctim.adc_get[0]--;
		if(adctim.adc_get[1])adctim.adc_get[1]--;
		if(adctim.adc_get[2])adctim.adc_get[2]--;
		if(adctim.adc_get[3])adctim.adc_get[3]--;

		if(discontim.discon)discontim.discon --;
	}
}
